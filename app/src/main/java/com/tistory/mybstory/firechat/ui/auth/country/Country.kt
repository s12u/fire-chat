fun getCountryByName(name: String): Country? {
    return try {
        val result = Country::class.nestedClasses.first {
            it.simpleName == name
        }
        result.createInstance() as Country
    } catch (e: IllegalArgumentException) {
        null
    } catch (e: NoSuchElementException) {
        null
    }
}
